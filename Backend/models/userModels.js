import mongoose from 'mongoose'

// Schema represents the attribute of tables ya models
const userSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
)

// model is like tables
const User = mongoose.model('User', userSchema)

export default User
